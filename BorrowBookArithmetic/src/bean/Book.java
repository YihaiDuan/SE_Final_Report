package bean;

public class Book {

	private int id;
	private String title;
	private String type;
	private String author;
	private double price;
	private String image;
	private String publics;
	private String isbn;
	private String guide;
	private String author_induce;
	private String induce;
	private String list;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getAuthor() {
		return author;
	}
	public void setAuthor(String author) {
		this.author = author;
	}
	public double getPrice() {
		return price;
	}
	public void setPrice(double price) {
		this.price = price;
	}
	public String getImage() {
		return image;
	}
	public void setImage(String image) {
		this.image = image;
	}
	public String getPublics() {
		return publics;
	}
	public void setPublics(String publics) {
		this.publics = publics;
	}
	public String getIsbn() {
		return isbn;
	}
	public void setIsbn(String isbn) {
		this.isbn = isbn;
	}
	public String getGuide() {
		return guide;
	}
	public void setGuide(String guide) {
		this.guide = guide;
	}
	public String getAuthor_induce() {
		return author_induce;
	}
	public void setAuthor_induce(String author_induce) {
		this.author_induce = author_induce;
	}
	public String getInduce() {
		return induce;
	}
	public void setInduce(String induce) {
		this.induce = induce;
	}
	public String getList() {
		return list;
	}
	public void setList(String list) {
		this.list = list;
	}
	
	
}
